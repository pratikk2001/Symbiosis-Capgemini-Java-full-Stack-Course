class IntegerDemo {
    public static void main(String[] args) {
        Float num = 12.56f;
        Float numb = 22.89f;
        
        // Converting Float to long
        long myVal = num.longValue();
		long myVala = numb.longValue();		// Use num or numb as needed
        System.out.println(myVal);
		System.out.println(myVala);
    }
}
